#include<bits/stdc++.h>
        using namespace std;
        int main()
        {
            int a=0,b=0;
            int n,m;
            cin>>n>>m;
            for(i=0;i<n;i++)
                a+=5;
            for(i=0;i<m;i++)
                b+=5;
            cout<<a<<" "<<b;
        }
//m+n operations, so TIME COMPLEXITY= O(m+n)



